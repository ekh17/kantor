package kantor;

public class LoginData {

	private String login;
	private String password;
	
	public String getLogin() 
	{
		return login;
	}
	
	public void setLogin(String _login) 
	{
		login = _login;
	}
	
	public String getPassword() 
	{
		return password;
	}
	
	public void setPassword(String _password) 
	{
		password = _password;
	}
	
}
